function sumar(nro1, nro2) {
    return nro1 + nro2;
}

function restar (nro1, nro2) {
    return nro1 - nro2;
}

function multiplicar(nro1, nro2) {
    return nro1 * nro2;
}

function dividir(nro1, nro2) {
    return nro1 / nro2;
}

module.exports = {
    sumar,
    restar,
    multiplicar,
    dividir
}
